              <h1 class="title-head">
                Sony xperia xa1 và xa1 ultra: nâng cấp cấu hình, thiết kế vuông vắn, camera 23mp
              </h1>
              <div class="post-details">
                <span class="author">
                  <i class="fa fa-user"></i> Bizweb
                </span>
                <span>
                  <i class="fa fa-calendar"></i> 23/02/2017
                </span>
              </div>
              <div class="article-image">
                <img src="//bizweb.dktcdn.net/100/289/080/articles/xa1-1.jpg?v=1515399069080" alt="sony-xperia-xa1-va-xa1-ultra-nang-cap-cau-hinh-thiet-ke-vuong-van-camera-23mp">
              </div>
              <div class="article-details">
                <div class="article-content">
                  <div class="rte">
                    <p>Ngoài hai mẫu điện thoại XZ,&nbsp;Sony&nbsp;còn đem đến&nbsp;MWC 2017&nbsp;hai phiên bản nâng cấp của
                      dòng XA là XA1 và XA1 Ultra. Thừa hưởng phong cách thiết kế của dòng XZ, hai phiên bản XA mới được
                      nâng cấp cấu hình mạnh hơn, trang bị camera chính 23MP của Xperia X năm ngoái để cạnh tranh cùng với
                      phân khúc tầm trung đang rất sôi động.</p>
                    <p>Nếu như Xperia XA năm ngoái mang thiết kế viền bo tròn các góc thì XA1 năm nay được làm vuông vắn, phù
                      hợp với người dùng là nam giới hơn. Viền màn hình 5” độ phân giải HD vẫn được làm mỏng, lớp kính 2.5D
                      cong nhẹ nhưng các góc thì rất góc cạnh. Xperia XA1 sử dụng chất liệu nhựa polycarbonate, giống với
                      chiếc Xperia X Compact, thay vì có thiết kế viền kim loại. XA1 Ultra có thiết kế phần cạnh trên và
                      dưới giống với Xperia XZ Premium, thiết kế cạnh cắt kim cương.</p>
                    <p>
                      <img src="//bizweb.dktcdn.net/100/004/439/files/xa1-6fb4ae08-708b-47be-81b7-2e79932b9c37.jpg?v=1489551546807">
                    </p>
                    <p>Ngoài việc cấu hình được nâng cấp lên nền tảng MediaTek Helio P20, Sony cũng nâng cấp bộ nhớ lên 3GB
                      cho XA1 và 4GB cho XA1 Ultra, bộ nhớ trong cũng được nâng lên 32GB và có tuỳ chọn 64GB cho XA1 Ultra.
                      Cảm biến vân tay vẫn chưa được tích hợp do đảm bảo một thiết kế mỏng 8 - 8,1 mm.</p>
                    <p>​Như đã đề cập ở phần đầu, Sony đã đưa cụm camera 23MP kích thước 1/2.3” của dòng Xperia X 2016 vào hai
                      phiên bản XA1 mới. Camera hỗ trợ công nghệ lấy nét lai hybrid, khởi động camera nhanh 0,6 giây. Riêng
                      với Xperia XA1 Ultra vẫn giữ được camera trước 16MP với chống rung quang học. Khá thú vị khi Sony vẫn
                      chưa trang bị OIS cho camera sau của bất kỳ một chiếc Xperia nào.</p>
                    <p>Theo dự kiến,&nbsp;Sony Xperia XA1&nbsp;sẽ sớm được phân phối tại Việt Nam vào tháng 4/2017 với mức giá
                      dự kiến trong khoảng 8 triệu đồng. Xperia XA1 Ultra sẽ có giá cao hơn một chút và sẽ được phân phối
                      trong mùa hè này.</p>
