<?php

function public_url($url = '')
{
    return base_url('assets/' . $url);
}

function friendly_url($string) {
	$string = utf8convert($string);
	$string = trim(preg_replace('/[\s\/]+/mu', ' ', str_replace('-', ' ', preg_replace('/([^\pL\.\/\-0-9\ ]+)/u', '', strip_tags($string)))));
	
	$tmp = explode('-', $string); // split $string to array by '-' character
	foreach($tmp as $key => $element) {
		$tmp[$key] = preg_replace('/[\s]+/mu', '-', trim($element));
	}
	$string = implode('_', $tmp);
	return strtolower($string);
}
function utf8convert($str) {
    if(!$str)
        return false;
    $utf8 = array(
            'a'=>'á|à|ả|ã|ạ|ă|ắ|ặ|ằ|ẳ|ẵ|â|ấ|ầ|ẩ|ẫ|ậ|Á|À|Ả|Ã|Ạ|Ă|Ắ|Ặ|Ằ|Ẳ|Ẵ|Â|Ấ|Ầ|Ẩ|Ẫ|Ậ',
            'd'=>'đ|Đ',
            'e'=>'é|è|ẻ|ẽ|ẹ|ê|ế|ề|ể|ễ|ệ|É|È|Ẻ|Ẽ|Ẹ|Ê|Ế|Ề|Ể|Ễ|Ệ',
            'i'=>'í|ì|ỉ|ĩ|ị|Í|Ì|Ỉ|Ĩ|Ị',
            'o'=>'ó|ò|ỏ|õ|ọ|ô|ố|ồ|ổ|ỗ|ộ|ơ|ớ|ờ|ở|ỡ|ợ|Ó|Ò|Ỏ|Õ|Ọ|Ô|Ố|Ồ|Ổ|Ỗ|Ộ|Ơ|Ớ|Ờ|Ở|Ỡ|Ợ',
            'u'=>'ú|ù|ủ|ũ|ụ|ư|ứ|ừ|ử|ữ|ự|Ú|Ù|Ủ|Ũ|Ụ|Ư|Ứ|Ừ|Ử|Ữ|Ự',
            'y'=>'ý|ỳ|ỷ|ỹ|ỵ|Ý|Ỳ|Ỷ|Ỹ|Ỵ',
            );
    foreach($utf8 as $ascii=>$uni)
        $str = preg_replace("/($uni)/i",$ascii,$str);
    return $str;
}

